# Syntax for slicing a list
# list_name[start:end]

days = ["Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Satday"]
mid_days = days[2:3]
print(mid_days)

numbers = [1, 2, 3, 4, 5]
print(numbers[:3])

print(numbers[2:])
print(numbers[:])

